import turtle
import random

b = turtle.Screen()
t = turtle.Turtle()
t.shape("turtle")
t.color("black", "red")
t.speed(1)
t.pensize(5)
t.penup()
t.goto(-200, 0)
t.pendown()
t.circle(100)
t.penup()
t.goto(0, 0)
t.pendown()
t.circle(100)
t.penup()
t.goto(200, 0)
t.pendown()
t.circle(100)
t.penup()
t.goto(-100, -100)
t.pendown()
t.circle(100)
t.penup()
t.goto(100, -100)
t.pendown()
t.circle(100)
t.penup()
t.goto(-300, -300)
turtle.mainloop()